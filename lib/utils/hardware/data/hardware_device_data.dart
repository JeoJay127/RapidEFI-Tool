            r'(^|[^0-9A-F])([0-9A-F]{4})[^0-9A-F]+([0-9A-F]{4})([^0-9A-F]|$)')
        .firstMatch(upper);
    if (match == null) return upper;
    return '${match.group(2)}-${match.group(3)}';
  }

